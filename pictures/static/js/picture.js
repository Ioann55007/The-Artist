function On(a) {
   a.style.width = '620px';       //На параметр а вешаем событие - увеличение размера
   a.style.height = '570px';       //На параметр а вешаем событие - увеличение размера
   }
function Off(a) {
   a.style.width= '280px';        //вешаем на а другое событие - возврат к исходному
   a.style.height = '300px';       //На параметр а вешаем событие - увеличение размера

}